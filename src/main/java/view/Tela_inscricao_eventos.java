    private void loadCategorias() {
        String[] categorias = new String[]{"Estudante", "Servidor", "Público Externo"};

        for (String categoria : categorias) {
            this.select_categoria.addItem(categoria);
        }
    }

    private void loadAtividades() {
        List<Atividade> atividades = new CSListarAtividades().execute(this.id_evento);
        if (atividades == null) {
            return;
        }

        for (Atividade a : atividades) {
            String titulo = a.getTitulo();
            String horaInicio = a.getHora_inicio().toString();
            String horaTermino = a.getHora_termino().toString();

            JPanel item = new JPanel();
            item.setLayout(new BoxLayout(item, BoxLayout.X_AXIS));
            item.setBackground(new Color(240, 240, 240));
            item.setBorder(BorderFactory.createEmptyBorder(5, 10, 5, 10));

            JLabel lblTitulo = new JLabel(titulo);
            lblTitulo.setPreferredSize(new Dimension(300, 20));
            JLabel lblHorario = new JLabel(" " + horaInicio + " - " + horaTermino);
            lblHorario.setForeground(Color.DARK_GRAY);

            item.add(lblTitulo);
            item.add(Box.createHorizontalGlue());
            item.add(lblHorario);

            pnl_atividades.add(item);
        }

        pnl_atividades.revalidate();
        pnl_atividades.repaint();
    }

    private void loadEvento() {
        Evento evento = new CSFindEventoById().execute(this.id_evento);

        if (evento == null) {
            JOptionPane.showMessageDialog(this, "Evento nao encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
            new Tela_Inicial().setVisible(true);
            this.dispose();
            return;
        }

        txt_titulo.setText(evento.getTitulo());
        txt_descricao.setText(evento.getDescricao());
        txt_formato.setText(evento.getFormato().toString());
        txt_data_inicio.setText(evento.getDataInicio().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        txt_data_termino.setText(evento.getDataTermino().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));

        User organizador = new CSFindUserByID().execute(evento.getId_organizador());
        if (organizador != null) {
            txt_organizador.setText(organizador.getNome());
        } else {
            txt_organizador.setText("Organizador desconhecido");
        }
    }

