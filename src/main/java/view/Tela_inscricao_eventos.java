    private void btn_inscreverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inscreverActionPerformed
        String categoria = select_categoria.getSelectedItem().toString().trim().toUpperCase();

        Inscricao inscricao = new Inscricao();

        inscricao.setId_evento(this.id_evento);
        inscricao.setCategoria(categoria);

        try {
            boolean sucesso = this.controller.store(inscricao);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Inscrição realizada com sucesso!", "Inscrição", JOptionPane.INFORMATION_MESSAGE);

                //TODO: renderizar a tela do cronograma do usuario
            }
        } catch (AuthenticationException | DomainException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_inscreverActionPerformed

